    private void Create_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Create_btnActionPerformed
        // TODO add your handling code here:
        String parentName = Student_ParentName_txt.getText();
        Date DOB = Student_DOB_Chooser.getDate();
        String gender = Student_Gender_Selector.getSelectedItem().toString();
        String mobileNo = Student_MobileNo_txt.getText();
        String address = Student_Address_txt.getText();
        String section = Student_Section_txt.getText();

        AuthManager.RegisterStudent(name,email,password,parentName,DOB,gender,mobileNo,address,section);

        Status status = AuthManager.getStatus();
        Status_Label.setText(status.getstatusMessage());

        switch (status.getErrorCode()) {
            case 1->{Student_ParentName_txt.requestFocus();}
            case 2->{Student_DOB_Chooser.requestFocus();}
            case 3->{Student_Gender_Selector.requestFocus();}
            case 4->{Student_MobileNo_txt.requestFocus();}
            case 5->{Student_Address_txt.requestFocus();}
            case 6->{Student_Section_txt.requestFocus();}
        }
    }//GEN-LAST:event_Create_btnActionPerformed

